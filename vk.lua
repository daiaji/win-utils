for i = 0, 9 do M[tostring(i)] = 0x30 + i end
for b = string.byte('A'), string.byte('Z') do M[string.char(b)] = b end
for i = 1, 24 do M['F' .. i] = 0x6F + i end

function M.normalize(key)
    if type(key) == 'number' then return key end
    if type(key) ~= 'string' then return nil end
    return M[key:upper()]
end

return M
